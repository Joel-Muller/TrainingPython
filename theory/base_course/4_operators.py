# arithmetics
print(' == arithmetic ==')
print(1 * 2 / 2 - 3 + 5)
print(5 % 2)  # Ex: repartition of items in boxes
print(5 // 2)

# assignment
var1 = 1
var1 = var1 + 2
var1 += 2

# comparison
print(' == comparison ==')
print(1 == 2)
print(1 != 2)
print(1 <= 2)

# logical operators
print(' == logical ==')
print(True and False)
print(True or False)
print(not False)
print(any([False, False, False, False, True]))
print(all([False, False, False, False, True]))

# identity operators
print(' == identity operators ==')
print(type(1) is int)

# membership operator
print(' == membership operator ==')
print('x' in 'axc')
print('x' in ['a', 'x', 'c'])
print('axc' in ['a', 'x', 'c'])
print(['a', 'x'] in ['a', 'x', 'c'])
